package com.ums.beans;

public class RNABean {
    
       private int rnaId;
       private String typeName;
       private String description;
       private String rnaImage;

    public int getRnaId() {
        return rnaId;
    }

    public void setRnaId(int rnaId) {
        this.rnaId = rnaId;
    }


    public String getTypeName() {
        return typeName;
    }

    public void setTypeName(String typeName) {
        this.typeName = typeName;
    }

  
    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }



    public String getRnaImage() {
        return rnaImage;
    }

    public void setRnaImage(String rnaImage) {
        this.rnaImage = rnaImage;
    }

    
}
